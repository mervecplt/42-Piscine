#include <stdio.h>

void ft_swap(int *a, int *b){
	
   int tmp;
   tmp = *a;
   *a = *b;
   *b = tmp;

}

int main()
{
	int a = 9;
	int b = 10;
	ft_swap(&a,&b);
	printf(" a = %d and b = %d", a,b);
}